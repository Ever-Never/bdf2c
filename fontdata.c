// default:
#define FONTDATA_PIXEL_AT_10(chardata, w, h, x, y) (*((chardata) + (((x) * (8) + (y)%8 ) / 8 + ((y)/8)*(w) )) & (unsigned char)(0x80 >> (((x) * (h) + (7-((y)%8)) ) % 8)) )

// 16x24
#define FONTDATA_PIXEL_AT_11(chardata, w, h, x, y) (*((chardata) + (((x) * (h) + (y)) / 8)) & (unsigned char)(0x01 << (((x) * (h) + (y)) % 8)) )

int
fontdata_draw (fontdata_t * pfontdata, unsigned char c, void *userdata, fontdata_cb_draw_t cb)
{
    int i;
    int j;
    uint8_t * data = NULL;
    int w;
    int h;

    assert (NULL != pfontdata);
    data = fontdata_get_char (pfontdata, c);
    if (NULL == data) {
        return -1;
    }

    w = fontdata_get_width(pfontdata);
    h = fontdata_get_height(pfontdata);
    switch (fontdata_get_type(pfontdata)) {
#define FNTDAT_SHOW(type) \
    case type: \
        for (i = 0; i < h; i ++) { \
            for (j = 0; j < w; j ++) { \
                if (FONTDATA_PIXEL_AT_##type(data, w, h, j, i)) { \
                    cb (userdata, j, i, 1); \
                } else { \
                    cb (userdata, j, i, 0); \
                } \
            } \
        } \
        break
        FNTDAT_SHOW(0);
        FNTDAT_SHOW(1);

        FNTDAT_SHOW(9);
        FNTDAT_SHOW(8);
        FNTDAT_SHOW(11);
    default:
        FNTDAT_SHOW(10);
    }
    return 0;
}


